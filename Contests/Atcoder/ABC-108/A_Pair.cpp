class Task {
 public:
  void Perform() {
    Read();
    Solve();
  }
 
 private:
  int k;  

  void Read() {
    cin >> k; 
  }
 
  void Solve() {
    int even = k / 2;
    int odd = k - even;
    cout << even * odd << '\n';
  }
};
 
int main() {
  ios_base::sync_with_stdio(false), cin.tie(nullptr);
  Task t;
  t.Perform();
  return 0;
}