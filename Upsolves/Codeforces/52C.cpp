using Node = int64_t;
using Query = int64_t;

Node Merge(Node a, Node b) {
  return Node{min(a, b)};
}

Node E() {
  return Node{INT64_MAX};
}

Node ApplyQuery(Query q, Node a) {
  return Node{a + q};
}

Query Id() {
  return Query{0};
}

Query Push(Query up, Query x) {
  return Query{x + up};
}

class Task {
 public:
  void Perform() {
    Read();
    Solve();
  }
 
 private:
  int n;
  vector<Node> a;

  void Read() {
    cin >> n;
    a.resize(n);
    for (auto &i : a) {
      cin >> i;
    }
  }
 
  void Solve() {
    atcoder::lazy_segtree<Node, Merge, E, Query, ApplyQuery, Push, Id> rmq(a); 
    int q;
    cin >> q;
    for (int tt = 0; tt < q; ++tt) {
      if (tt == 0) {
        cin.ignore();
      }
      string s;
      getline(cin, s);
      stringstream line_stream(s);
      vector<int> line;
      while (line_stream >> s) {
        line.push_back(stoi(s));
      }
      int l = line[0], r = line[1];
      if (int(line.size()) == 2) {
        int64_t ans;
        if (l <= r) {
          ans = rmq.prod(l, r + 1);
        } else {
          ans = Merge(rmq.prod(l, n), rmq.prod(0, r + 1));
        }
        cout << ans << '\n';
      } else {
        Query qq = line[2];
        if (l <= r) {
          rmq.apply(l, r + 1, qq);
        } else {
          rmq.apply(l, n, qq);
          rmq.apply(0, r + 1, qq);
        }
      }
    }
  }
};
 
int main() {
  ios_base::sync_with_stdio(false), cin.tie(nullptr);
  Task t;
  t.Perform();
  return 0;
}